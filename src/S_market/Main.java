    private void jP24MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jP24MousePressed

        try {
            //Obtain only one instance of the SystemTray object
            SystemTray tray = SystemTray.getSystemTray();
            //Creating a tray icon
            ImageIcon icon = new ImageIcon(getClass().getResource("/image/icons8_Grid_2_48px.png"));
            Image image = icon.getImage();
            //System.out.println(image);
            TrayIcon trayIcon = new TrayIcon(image);
            //Let the system resizes the image if needed
            trayIcon.setImageAutoSize(true);
            //Set tooltip text for the tray icon
            trayIcon.setToolTip("رسالة تنبية");
            tray.add(trayIcon);
            trayIcon.displayMessage("تنبيه !", "منتج علي وشك النفاذ", MessageType.NONE);
        } catch (AWTException ex) {
            Exceptions.printStackTrace(ex);
        }

    }//GEN-LAST:event_jP24MousePressed

