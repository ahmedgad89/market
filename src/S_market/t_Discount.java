    private void bt_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_saveActionPerformed

        try {

            dat_from = ((JTextField) date_da_from.getDateEditor().getUiComponent()).getText();
            dat_in = ((JTextField) date_da_to.getDateEditor().getUiComponent()).getText();

            if (combo_product_name.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "من فضلك ادخل اسم المنتج", "خطاء", JOptionPane.ERROR_MESSAGE);
                combo_product_name.requestFocus();

            } else if (txt_discount_quantity.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "من فضلك ادخل  الكمية المراد خصمها", "خطاء", JOptionPane.ERROR_MESSAGE);
                txt_discount_quantity.requestFocus();

            } else if (dat_from.isEmpty()) {
                JOptionPane.showMessageDialog(null, "من فضلك ادخل تاريخ الخصم من  ", "خطاء", JOptionPane.ERROR_MESSAGE);

            } else if (dat_in.isEmpty()) {

                JOptionPane.showMessageDialog(null, "من فضلك ادخل تاريخ الخصم الي  ", "خطاء", JOptionPane.ERROR_MESSAGE);

            } else if (dat_from.compareTo(dat_in) > 0) {
                JOptionPane.showMessageDialog(null, "قد ادخلة تاريخ  خطاء", "خطاء", JOptionPane.ERROR_MESSAGE);

                date_da_from.setDate(null);
                date_da_to.setDate(null);
                dat_from = "";
                dat_in = "";

            } else if (txt_discount_value.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "من فضلك ادخل قيمة الخصم", "خطاء", JOptionPane.ERROR_MESSAGE);
                txt_discount_value.requestFocus();
            } else {

                int r = JOptionPane.showConfirmDialog(null, "هل تريد حقا حفظ البيانات", "حفظ بيانات", JOptionPane.YES_NO_OPTION);

                if (r == JOptionPane.YES_OPTION) {

                    String[] values = {null, product_name, comboBox_discount_type.getSelectedItem().toString(),
                        txt_discount_value.getText(), txt_discount_quantity.getText(), dat_from, dat_in, null, null, "0", user};

                    int res = db.add("td_discount", values, null);
                    // int res= tdb.add("tb_employees", values,null);

                    if (res == -1) {
                        JOptionPane.showMessageDialog(null, "تم حفظ البيانات بنجاح");

                        //تعديل
                        Update_discount();
                        EmbteTex();

                    } else {
                        JOptionPane.showMessageDialog(null, "لم تتة الاضافه", "خطاء", JOptionPane.ERROR_MESSAGE);
                    }

                }

            }
        } catch (Exception ex) {
            Exceptions.printStackTrace(ex);
        }
        try {
            db.conn().close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_bt_saveActionPerformed

