    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked

        try {
            row = table.getSelectedRow();
            String tableClick = (table.getValueAt(row, 0).toString());
            ResultSet rs = db.selecte("td_discount", new String[]{"*"}, new String[]{"id"}, new String[]{tableClick}, "=", "and");

            try {
                if (rs.next()) {

                    combo_product_name.setSelectedItem(rs.getString("product_name"));
                    if (rs.getString("discount_type").equals("نسبة")) {
                        comboBox_discount_type.setSelectedIndex(0);
                    } else {
                        comboBox_discount_type.setSelectedIndex(1);
                    }
                    txt_discount_value.setText(rs.getString("discount_value"));
                    txt_discount_quantity.setText(rs.getString("discount_quantity"));
                    date_da_from.setDate(rs.getDate("date_from"));
                    date_da_to.setDate(rs.getDate("date_to"));

                    bt_delete.setEnabled(true);
                    bt_update.setEnabled(true);
                    bt_save.setEnabled(false);
                    bt_new.setEnabled(false);

                }
            } catch (SQLException e) {

                JOptionPane.showMessageDialog(null, e, "خطاء", JOptionPane.ERROR_MESSAGE);
            }

            try {
                db.conn().close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "خطاء", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_tableMouseClicked

